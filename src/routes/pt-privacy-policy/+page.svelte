        <h2>Notice of Privacy Practices (HIPAA)</h2>
        
        <p class="hipaa-notice"><strong>THIS NOTICE DESCRIBES HOW MEDICAL INFORMATION ABOUT YOU MAY BE USED AND DISCLOSED AND HOW YOU CAN GET ACCESS TO THIS INFORMATION. PLEASE REVIEW IT CAREFULLY.</strong></p>

        <p>Rapid Recovery: Sports Therapy and Performance ("we", or "us") is committed to providing you with the highest quality of care in a friendly and encouraging environment that protects your privacy and the confidentiality of your health information. As such, this notice explains our privacy practices, as well as your rights, regarding your health information.</p>

        <h3>Your Rights</h3>
        <p>You have the following rights regarding health information we maintain about you. This section explains your rights and how to exercise them.</p>

        <h4>Ask Us to Restrict What We Use or Share</h4>
        <p>You can ask us not to use or share certain health information for treatment, payment, or our operations. We are not required to agree to these requests. For example, we may deny if it would affect your care.</p>
        <p>If you pay for a service or health care item out-of-pocket in full, you can ask us not to share that information for the purpose of payment or our operations with your health insurer. Requests will be honored unless a law requires us to share that information.</p>

        <h4>Request Confidential Communications</h4>
        <p>You can ask us to contact you in a specific way (for example your work phone) or to send mail to a different address. We will honor all reasonable requests.</p>

        <h4>Obtain an Electronic or Paper Copy of Your Medical Record</h4>
        <p>You can ask to inspect or get an electronic or paper copy of your medical record and other health information we maintain about you.</p>
        <p>We will provide a copy or a summary of your health information, usually within 30 days of your request. We may charge a reasonable, cost-based fee.</p>

        <h4>Ask Us to Correct or Amend Your Medical Record</h4>
        <p>You can ask us to correct health information about you that you think is incorrect or incomplete.</p>
        <p>We may deny your request, but we will inform you in writing, usually within 60 days of your request. To request an amendment your request must be made in writing, submitted to the Privacy Officer.</p>

        <h4>Obtain a List of Those With Whom We've Shared Your Information</h4>
        <p>You can ask us for a list (accounting) of the instances we have shared your health information for six years prior to the date you ask, with whom we shared it, and why.</p>
        <p>We will include all the disclosures except for those about treatment, payment, or health care operations, and certain other disclosures (such as any you asked us to make or other authorized disclosures). We will provide one accounting per year for free but may charge a reasonable, cost-based fee if you ask for another one within 12 months. To request this list of disclosures, you must submit your request in writing to the Privacy Officer.</p>

        <h4>Obtain a Copy of This Privacy Notice</h4>
        <p>You can ask for a paper copy of this notice at any time, even if you have agreed to receive the notice electronically. We will provide you with a paper copy promptly.</p>

        <h4>Choose Someone to Act for You</h4>
        <p>If you have given someone medical power of attorney or if someone is your legal guardian, that person (your "personal representative") can exercise your rights and make choices about your health information.</p>
        <p>If someone has been appointed to act for you, a copy of the document appointing that person must be provided to us. We will make reasonable efforts to verify that the person has the authority and can act for you before we take any action.</p>

        <h3>Our Responsibilities</h3>
        <ul>
            <li>We are required by law to maintain the privacy and security of your protected health information.</li>
            <li>We will let you know promptly if a breach occurs that may have compromised the privacy or security of your information.</li>
            <li>We must follow the duties and privacy practices described in this Notice and offer you a copy of it.</li>
            <li>We will not use or share your information other than as described here unless you tell us we can do so in writing. If you tell us we can, you may change your mind at any time. Let us know in writing if you change your mind.</li>
        </ul>

        <h3>Changes to This Notice</h3>
        <p>We reserve the right to change this notice. We reserve the right to make the revised or changed notice effective for health information we already have about you as well as any information we receive in the future. We will post a copy of the current notice in our facility.</p>
        <p>The notice will contain on the first page, the effective date. In addition each time you register for treatment or health care services, we will offer you a copy of the current notice in effect.</p>

        <h3>You May File a Complaint</h3>
        <p>If you believe your privacy rights have been violated or you have questions regarding this notice please contact the Privacy Officer by emailing <strong>support@rapidrecoverypt.org</strong>.</p>
        <p>You may also file a complaint with the Office of Civil Rights US Department of Health and Human Services by sending a letter to 200 Independence Ave. S.W., Washington, D.C 20201, call 1.877.696.6775 or visit www.hhs.gov/ocr/privacy/hipaa/complaints.</p>
        <p><strong>Filing a complaint will not affect the treatment or services you receive.</strong></p>
        <p>Please contact us to exercise any of the above items using the information at the end of this Notice. You may have to complete a form and submit your request in writing. For example, to request an amendment of your record you must fill out a form.</p>

        <h3>This Notice Will be Followed By:</h3>
        <p>All organizations required to have a Notice of Privacy Practices and owned or controlled by Rapid Recovery Sports Therapy and Performance LLC (referred to throughout this Notice as "we," or "us).</p>

        <h3>How Rapid Recovery: Sports Therapy and Performance May Use and Share Your Health Information</h3>
        <p>We may, without your written permission, use your health information within Rapid Recovery: Sports Therapy and Performance and share or disclose your health information to others outside Rapid Recovery: Sports Therapy and Performance in the following ways:</p>

        <h4>For Treatment, Payment, and Health Care Operations</h4>
        <p>Rapid Recovery: Sports Therapy and Performance may use and disclose your health information without your written authorization for treatment, payment, and health care operations.</p>
        
        <p><strong>For Treatment:</strong> We may use your personal health information to provide you with health care treatment or services. We may share your health information with doctors, nurses, health students, or other personnel who are involved in your care. For example, a treating therapist may ask another healthcare provider about your condition to assist in treating you.</p>
        
        <p><strong>For Payment:</strong> We may use and disclose your personal health Information to help us or another provider obtain payment for the healthcare services provided. For example, we may need to give your health plan information about your treatment session so your health plan will pay us or reimburse you for the service. We may also tell your health plan about a treatment you are going to receive to obtain prior approval or to determine whether your plan will cover the cost of treatment.</p>
        
        <p><strong>For Health Care Operations:</strong> We may use your health information to support our business practice activities and improve the quality and cost of care. For example, we may use health information to review our treatment and services and to evaluate the performance of our staff in caring for you. We may use your health information to contact you at the address and telephone number(s) you provide (including leaving a message at the telephone numbers) about scheduled or cancelled appointments, registration/insurance updates, billing and/or payment matters.</p>

        <h4>Other Instances That Require Use or Disclosure of Your Personal Health Information</h4>
        <p>Rapid Recovery: Sports Therapy and Performance may use or disclose your personal health information to others without your permission in various other ways, typically to assist the general public including public policy and research. Prior to sharing your information Rapid Recovery: Sports Therapy and Performance must adhere to the various conditions in the privacy law before we share your information. Examples include:</p>
        
        <ul>
            <li><strong>Research:</strong> Under certain circumstances, we may use and disclose medical information about you for research purposes, or we may contact you about research projects that you may qualify for. Occasionally, you may be asked to give authorization before we share your information with others for use in research.</li>
            <li><strong>Fundraising:</strong> For fundraising purposes to support Rapid Recovery: Sports Therapy and Performance and its affiliated foundation mission provided, however, that such information is limited to demographic information only such as name, address, phone number, age or gender.</li>
            <li><strong>As Required by Law:</strong> We will disclose health information about you when required to do so by federal, state or local law.</li>
            <li><strong>To Avert a Serious Threat to Health or Safety:</strong> We may use and disclose your health information when necessary to prevent a serious threat to your health and safety or the health and safety of the public or another person.</li>
            <li><strong>Other Permitted Uses:</strong> Military and Veterans, Workers Compensation, Individuals Involved in Your Care or Payment for Your Care, Public Health Risks, Health Oversight Activities, Lawsuits and Disputes, Law Enforcement, National Security and Intelligence Activities, Protective Services for the President and Others, and Inmates: We may use or disclose health information in these circumstances as permitted or required by law.</li>
        </ul>

        <h4>Other Uses of Health Information</h4>
        <p>Other uses and disclosures of health information not covered by this notice or the laws that apply to use will be made only with your written authorization including disclosures that constitute the sale of your health information or disclosures related to marketing outside of face-to-face and promotional gifts of nominal value that are permitted under the law. If you provide us permission to use or disclose health information about you, you may revoke that permission, in writing, at any time.</p>

        <h3>Acknowledgment of Receipt of This Notice</h3>
        <p>We will request that you sign a separate form or notice acknowledging you have been offered a copy of this notice. If you choose, or are not able to sign, a staff member may sign his/her name and date. This acknowledgement will be filed with your records.</p>

